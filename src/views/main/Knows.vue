                      <p>
                        明代襦裙明代上襦下裙的服装形式，与唐宋时期的襦裙没有什么差别，只是在年轻妇女中间，常加一条短小的腰裙，以便活动，有些侍女丫环也喜欢这种装束。上襦为交领、长袖短衣。裙子的颜色，初尚浅淡，虽有纹饰，但并不明显。至崇祯初年，裙子多为素白，即使刺绣纹样，也仅在裙幅下边一、二寸部位缀以一条花边，作为压脚。裙幅初为六幅，即所谓“裙拖六幅湘江水”；后用八幅，腰间有很多细褶，行动辄如水纹。到了明末，裙子的装饰日益讲究，裙幅也增至十幅，腰间的褶裥越来越密，每褶都有一种颜色，微风吹来，色如月华，故称“月华裙”。
