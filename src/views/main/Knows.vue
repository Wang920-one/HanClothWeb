                <el-tab-pane label="袍服制">
                  <div style="background-color:#ffffff70;">
                    <h4>
                      上下通裁制
                    </h4>
                    <p>
                      即用一块布裁出上衣和下衣，中间无接缝，自然一体，明显区别于上衣下裳制和深衣制。这一形制始创于隋唐，这在当时来说是一个创举，因为自古汉服都是分上下两截的。通裁制的种类很多，圆领袍、襕衫、直裰、直身、道袍、褙子、长衫、僧衣等。这些最流行的时期在宋代和明代，皇帝贵族平时也喜欢这么穿，更是文人骚客们的休闲装。
                    </p>
                    <p>1.圆领袍</p>
                    <p>
                      汉代随着服饰逐渐统一，圆领袍主要作为内衣穿着。溯其由传人以至兴盛，由军用以至民服，历史悠长，变化复杂，圆领亦称团领，实为无领型领式。衣领形似圆形，内覆硬衬，领口钉有纽扣。
                    </p>
                    <img
                      src="@/assets/images/knows/xingzhi/李白圆领袍.jpg"
                      class="image"
                    />
                    <p>2.道袍</p>
                    <p>
                      一种源自中国古代汉服“褶”(相传周武王亲制)，在明代演变为道袍，是明代男子居家时的外衣，也可作衬袍或平民男子婚服。后在清初“剃发易服”的政策下消亡。
                      其特点是直领，大襟，两侧开衩，有暗摆，为汉民族最具特色的传统服饰之一。
                    </p>
                    <img
                      src="@/assets/images/knows/xingzhi/道袍.jpg"
                      class="image"
                    />
                    <p>3.褙子</p>
                    <p>
                      褙子，又名背子、绰子、绣䘿，是汉服传统服饰的一种，始于隋朝。褙子直领对襟，两侧从腋下起不缝合，多罩在其他衣服外穿着，流行于宋、明两朝。
                    </p>
                    <img
                      src="@/assets/images/knows/xingzhi/褙子.png"
                      class="image"
                    />
                    <p>4.直裰</p>
                    <p>
                      直裰起源于唐朝，是从宋朝开始就有的一种汉服服饰。据宋
                      赵彦卫《云麓漫钞》谓：“古之中衣，即今僧寺行者直裰。”两宋时期的直裰多为僧侣穿着（少数文人也有穿着）。
                    </p>
                    <img
                      src="@/assets/images/knows/xingzhi/直裰.jpg"
                      class="image"
                    />
                  </div>
                </el-tab-pane>
                <el-tab-pane label="襦裙制">
                  <div style="background-color:#ffffff70">
                    <h4>
                      上衣下裳的演变
                    </h4>
                    <p>
                      襦即短上衣。襦裙不是指一种裙子，而是上襦加下裙，是一套服饰的统称。
                    </p>
                    <p>
                      襦裙属于汉服的一种，上身穿的短衣和下身束的裙子合称襦裙，是典型的“上衣下裳”衣制。上衣叫做“襦”，长度较短，一般长不过膝，下身则叫“裙”。
                    </p>
                    <p>
                      襦裙的本质还是上衣下裳制，古老的上衣下裳发展到春秋战国之后往往称为襦裙，汉朝以后又被特指为女子襦裙：短衣长裙，腰间以绳带系扎，衣在内，裙在外。各朝各代在襦裙的基本形制下衍生出高腰襦裙、半臂襦裙、对襟襦裙、齐胸襦裙等款式，这些款式没有很多的礼仪规定，一般是用于常服的，所以普及面很广。
                    </p>
                    <p>
                      襦裙又分为齐腰襦裙、高腰襦裙、齐胸襦裙等。搭配半臂穿着又称半臂襦裙，可搭配披帛。披帛，又称“画帛”，通常一轻薄的纱罗制成，上面印画图纹。长度一般为二米以上，用时将它披搭在肩上，并盘绕于两臂之间。
                    </p>
                    <p>
                      除此之外，还有上衣下裤形制等等。上衣下裤是历代男子常服之一，用于劳作及日常穿着等，十分方便，是劳动者的工作服。
                    </p>
                    <p>1.交领襦裙</p>
                    <p>
                      特点是上襦为交领，交领襦裙在宋代颇为常见。
                    </p>
                    <img
                      src="@/assets/images/knows/xingzhi/交领襦裙.png"
                      class="image"
                    />
                    <p>2.对襟襦裙</p>
                    <p>
                      其上襦为直领，衣襟呈对称状，故称对襟襦裙。
                    </p>
                    <img
                      src="@/assets/images/knows/xingzhi/对襟襦裙.png"
                      class="image"
                    />
                    <p>3.坦领襦裙</p>
                    <p>
                      坦领，又称U领，汉服半臂中的一种，是从魏、晋以来上襦发展出的一种短外衣。由于胡文化在当时的唐代各阶层间的广泛流行，使得唐代女性穿小袖衣成为一种风尚。流行于隋代宫廷内，先为宫中内官、女史所服，唐代传至民间，历久不衰。
                    </p>
                    <img
                      src="@/assets/images/knows/xingzhi/坦领襦裙.png"
                      class="image"
                    />
                    <p>4.齐胸襦裙</p>
                    <p>
                      齐胸襦裙是汉民族传统服饰的的一种，汉晋以来裙子的裙腰束于腰上，而隋唐五代时期裙子的裙腰束得更高，很多都在胸上，一些服装史上多称之为高腰襦裙，根据现在人们对它的考证，一般改称之为齐胸襦裙，齐胸襦裙至今没有服装文物出土。齐胸襦裙属于早期复原错误的称呼，正确的叫法应为齐胸衫裙。
                    </p>
                    <img
                      src="@/assets/images/knows/xingzhi/齐胸襦裙.png"
                      class="image"
                    />
                  </div>
                </el-tab-pane>
                <el-tab-pane label="华夏衣裳">
                  <div style="background-color:#ffffff70;;height:800px">
                    <p>
                      总结： 最开始的上衣下裳制以及深衣制不限男女，
                      但后来男子着裙裳逐渐被长衫袍服所代替，“上衣下裳”和深衣由日常生活逐渐退隐到礼仪祭祀中去，成为汉服礼服。春秋战国秦汉时期女子着衣与男子款式大致相同，到了汉代以后，女子汉服又从从连体的深衣重新回归到上古上下两截的穿法。隋唐时常说的襦裙装，上襦下裙，其实就是“上衣下裳”的一种，只不过襦稍短些、裙稍长些，它其实也是上衣掖在裙里，只不过将裙提到了胸部。到了明代，基本上相当于又一次复礼，恢复了上古那种正统的“衣裳”制式。
                    </p>
                    <p>
                      汉服四大形制，“上衣下裳”的古制是基础，深衣制是其发展，袍服制其实就是把上衣加长，而襦裙制则是把上衣缩短，分别向不同的方向发展出“上衣下裳”的各种变形。
                    </p>
