                      <h4>隋唐汉服（589年左右）</h4>
                      <p>
                        公元581年，隋朝的建立，结束了自东汉以来相继四百年的分裂局面。这是继秦汉之后再度建立的封建统一国家，一个以汉族为中心的新的民族共同体。南北两地服装彼此仿效，业以合壁。唐朝时中国封建社会的鼎盛时期，揭开了中国古代最为灿烂夺目的篇章。唐朝时国家稳定，经济繁荣、文化事业全面发展，封建文化已经达到高峰。近三百年的唐代服饰经过长期的承袭、演变、发展成为中国服装发展上一个极为重要的时期。一方面唐代服装上承历代冠服制度，下启后世衣冠之径道；另一方面唐朝服装发展兼容并蓄，广采博收，大放异彩。唐以后的五代十国是唐宋封建军阀割据的继续，在服饰上大体沿袭唐朝之制。隋唐服装无论官服或民服，男装和女装，都表现其开放的思想、开拓的精神，充分反映了鲜明的时代性和强烈的民族性。
                      </p>
                      <img
                        src="@/assets/images/knows/history/隋唐汉服.jpg"
                        class="image"
                      />
                      <P>
                        襦裙是唐代妇女的主要服式。在隋代及初唐时期，妇女的短襦都用小袖，下着紧身长裙，裙腰高系，一般都在腰部以上，有的甚至系在腋下，并以丝带系扎，给人一种俏丽修长的感觉。披帛，又称“画帛”，通常一轻薄的纱罗制成，上面印画图纹。长度一般为二米以上，用时将它披搭在肩上，并盘绕于两臂之间。唐代妇女，以体态丰腴为美。由于身材丰硕，中唐女服也渐趋宽大，裙子的宽度比隋末唐初时要肥大的多。衣衫虽为小袖，但与初唐、盛唐女服相比，也明显趋于宽松。
                      </P>
                      <img
                        src="@/assets/images/knows/history/隋唐汉服1.jpg"
                        class="image"
                      />
                      <P>
                        唐代大袖衫盛唐以后，胡服的影响逐渐减弱，女服的样式日趋宽大。到了中晚唐时期，这种特点更加明显，一般妇女服装，袖宽往往四尺以上。中晚唐之际的贵族礼服，一般多在重要场合穿着，如朝参、礼见及出嫁等。穿着这种礼服，发上还簪有金翠花钿，所以又称“钿钗礼衣”。大袖衫裙样式为大袖、对襟，佩以长裙、披帛。以纱罗作女服的衣料，是唐代服饰中的一个特点，这和当时的思想开放有密切关系。尤其是不着内衣，仅以轻纱蔽体的装束，更是创举，所谓“绮罗纤缕见肌肤”，就是对这种服装的概括。
                      </P>
                      <img
                        src="@/assets/images/knows/history/隋唐汉服2.jpg"
                        class="image"
                      />
                      <P>
                        隋唐时代也产生了汉服的一种
                        重要变体圆领衫。圆领式样在中国服饰历史上很早便有出现，但一直到隋唐才开始盛行，成为官式常服。这种服装延续了唐，五代，宋，明，并对日本，高丽等国产生了很大的影响。裹幞头、穿圆领袍衫是唐代男子的普遍服饰，以幞头袍衫为尚。幞头又称袱头，是在汉魏幅巾基础上形成的一种首服。唐代以后，人们又在幞头里面增加了一个固定的饰物，名为“巾子”。巾子的形状各个时期有所不同。除巾子外，幞头的两脚也有许多变化，到了晚唐五代，已由原来的软脚改变成左右各一的硬脚。
                      </P>
                      <img
                        src="@/assets/images/knows/history/隋唐汉服3.jpg"
                        class="image"
                      />
                      <P>
                        需要指出的是，这种圆领汉服变体的盛行并不代表传统右衽交领汉服的消失。实际上，即便是在其最盛行的唐朝，也多局限于以长安为中心的地区，以及官员，皇室成员之中，而在如吴越地区以及普通百姓之间，则以大襟右衽交领这种汉服为主，宋明类似。唐代官吏，除穿圆领窄袖袍衫之外，在一些重要场合，如祭祀典礼时仍穿礼服。礼服的样式，多承袭隋朝旧制，头戴介帻或笼冠，身穿对襟大袖衫，下着围裳，玉佩组绶等。圆领汉服和交领汉服一样，是汉民族服饰的重要组成部分。
                      </P>
                      <img
                        src="@/assets/images/knows/history/隋唐汉服4.jpg"
                        class="image"
                      />
                      <P>
                        上衣下裳：仍有。
                      </P>
                      <P>
                        衣裳连制（深衣）：仍就是士大夫居家和民间小礼服。
                      </P>
                      <P>
                        袍衫：袍衫可谓空前盛行，成为民间的主流服饰，当时品官常服开始采用圆领袍，一直延续到明，也就是我们后来说的官袍或公服。
                      </P>
                      <p>
                        上襦下裙：那时女子的襦裙，上襦变短只到胸部，裙子变宽大，系到胸部，然后系以长裙带几乎垂地，类似于现在的朝鲜女装。袖子基本上都变成了小袖，后期小袖垂下一部分，遮住手，显得纤细，成为垂袖。一开始的时候襦裙上披一块又方又厚类似于披风的东西，成为帔子。后来变长，变窄，只成一布条，固定在肩膀上，披法花样繁多，成为披帛。后来，帔子和披帛成为襦裙中不可分割的一元素，一直延续到明。
                      </p>
                      <p>
                        中唐到五代时期的襦裙，风格又回归到了魏晋时期，襦仍是到腰胯，裙子变窄，裙腰高耸，只不过袖子还是小袖。
                      </p>
                      <p>
                        阑衫：阑衫本属于袍衫类，因隋唐时期男子官服，本穿的是杉，但长度只到膝，膝处设一界限，称为横阑，下面则是用裳续上。因此，他虽然大范畴属于泡衫类，但构造上更像泡衫和深衣的综合体，因此，又成了漢服体系中的第6个款式——阑衫类。因早期阑衫是作为官服出现的，领子都用圆领，而后来的阑衫，大致也遵循了这一点，只不过领边变宽，并且接衽了。明时为有官职人员的公服。
                        直裰：前面提到前朝上下通裁的长衣，像袍衫出自长衣。而原本的长衣却没有形成定制。但是到了隋唐时期，长衣规定长度到脚踝，并且要有宽衣缘，而且都是交领，两侧不能缺胯（开叉），因此又形成了漢服体系中的第7个款式——直裰（直身）类。当时直裰的定制，主要是想复兴古礼，找个深衣类与袍衫类的最佳中间点，因此直裰，也是最具有规格的长衣便应运而生了。
                      </p>
                      <p>
                        袄子——大袖衣——旋袄——褙子：当时流行一种襦，但不同的是直领相系，并且两侧开叉到腋下，这样就有点半披半穿的感觉，当时称为袄子。后来女子礼服用的大袖衣，也是参照这个，开叉到腋下，直领相系，之所以叫大袖衣，只不过袖子是大袖，衣稍长点而已。后来到宋朝时，变窄变长，袖子又变成小袖，这回还是直领，但就是不相系了，成为旋袄。明时又变宽，袖子变大，变成半直领相系（有带子）下面自然开叉。因这一系列都是一脉相成下来而且没有款式上的变化，因此他们又可以归为一类，成为漢服体系中的第8个款式——统称为褙子类。褙子最主要的特点就是直领相系，两侧开叉到腋下，初期多用于礼服，后来成为定制，比如女子多穿襦裙，妇人和民间礼仪活动时多穿褙子。这也就是朝鲜时期的宫廷唐衣（韩剧中王妃尚宫所穿的交领褙子不过。到明又出现无袖和半袖的褙子，当时已经男女通用了。
                      </p>
                      <p>
                        半臂：当时又流行一种襦，是半袖和无袖的，多是直领相系，有小带子系到胸部。如果另算的话，也可以算做漢服体系中的第9个款式——半臂类。由于是半袖和无袖的，一般都当套在襦裙外的外套穿。不过我看他更应该归纳到襦里面。
                      </p>
                      <p>
                        冠帽巾：除了冠巾不变之外，此时是帽类空前发展的一个时期。如幞头，最初的无翅——到软翅——到硬翅——到直翅——再到展翅，直翅后来成为明朝品官的乌纱帽，展翅后来成为宋朝品官的乌纱帽。如斗笠，在那时也产生。如女子的帷帽，面幕。
                      </p>

