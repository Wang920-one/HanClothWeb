                      <h4>魏晋南北朝汉服（220年左右）</h4>
                      <p>
                        魏晋时期，风流名士们崇尚自然、超然物外，率真任诞而风流自赏。姿容飘逸的魏晋风度也反映到了汉服上，这一时期的男子一般都穿大袖衫——大袖翩翩的衫子，上自王公名士，下及黎庶百姓，都以宽衫大袖，褒衣博带为尚。直到南朝时期，这种衫子仍为各阶层男子所爱好。衫和袍在样式上有明显的区别，照汉代习俗，凡称为袍的，袖端应当收敛，并装有祛口。而衫子却不需施祛，袖口宽敞。魏晋服装日趋宽博，而衫由于不受衣祛等部约束，故成为一时风俗。
                      </p>
                      <img
                        src="@/assets/images/knows/history/魏晋南北朝.jpg"
                        class="image"
                      />
                      <p>
                        魏晋南北朝时期的妇女的衫裙魏晋时期妇女服装承袭秦汉的遗俗，并吸收少数民族服饰特色，在传统基础上有所改进，一般上身穿衫、袄、襦，下身穿裙子，腰用帛带系扎，款式多为上俭下丰，以宽博为主，其特点为：对襟，束腰，衣袖宽大，并在袖口、衣襟、下摆缀有不同色的缘饰，下着条纹间色裙，腰间用一块帛带系扎。衣身部分紧身合体，袖口肥大，裙为多折裥裙，裙长曳地，下摆宽松，从而达到俊俏潇洒的效果。加上丰盛的首饰，反映出奢华糜丽之风。当时妇女的下裳，除间色裙外，还有其它裙式。
                      </p>
                      <img
                        src="@/assets/images/knows/history/魏晋南北朝1.jpg"
                        class="image"
                      />
                      <p>
                        魏晋南北朝时期的杂裾服传统的深衣制已不被男子采用，但在妇女中间却仍有人穿着。这种服装与汉代相比，已有较大的差异。在衣服的下摆部位，加一些饰物，通常以丝织物制成。其特点是上宽下尖形如三角，并层层相叠。另外，由于从围裳中伸出来的飘带比较长，走起路来，如燕飞舞。到南北朝时，这种服饰又有了变化，去掉了曳地的飘带，而将尖角的“燕尾”加长，使两者合为一体。
                      </p>
                      <img
                        src="@/assets/images/knows/history/魏晋南北朝2.jpg"
                        class="image"
                      />
                      <p>
                        南北朝时期裤褶：裤褶的基本款式为上身穿齐膝大袖衣，下身穿肥管裤。这种服装的面料，常用较粗厚的毛布来制作。穿裤和短上襦，合称襦裤，但封建贵族必须在襦裤外加穿袍裳，只有骑马者、厮徒等从事劳动的人为了行动方便，才直接把裤露在外面。封建贵族是不得穿短衣和裤外出的。到了晋代这种习惯才有所改变。南北朝的裤有大口裤和小口裤，以大口裤为时髦，穿大口裤行动不便，故用锦带将裤管缚住，有、又称缚裤。
                      </p>
                      <p>
                        服色制度：由于九品中正制，品官等级被正式划分鲜明，设置了九品官，每级的礼服常服的颜色都要不一样。从此以后，服色制度成为礼服等级的一个标准，而以前，则多用绶佩区分，不用服色。
                        上衣下裳：由于魏晋又是一个思想大动荡的时期，常服中上衣下裳装束已不受人们所宠爱，由于受一部分思潮影响，衣裳日趋宽大飘逸，袖也多变为敞口的魏晋风骨。
                      </p>
                      <P>
                        衣裳连制（深衣）：深衣也退居到士大夫阶层和民间小礼服阶层，并且衣比之前朝更加宽大。民间似乎更喜欢新兴的袍衫和褶绔。
                      </P>
                      <P>
                        袍衫：很受欢迎，已开始有园领袍衫。
                      </P>
                      <P>
                        上衣下裤（褶绔）：应该说，前朝已有上短衣下裤的装束了，但那时的搭配较之随意，，而且下裤种类繁多，不宜构成固定款式。但当时发展出来的褶绔，成为主流，而且搭配固定，因此是为漢服体系中的第5个款式——上衣下裤（褶绔）类。具体是，上穿短衣，下穿裤子，比较宽大，呈上窄下宽喇叭状，带有褶皱。有时在膝上用带子捆绑或扎系，成为缚绔。
                      </P>
                      <P>
                        上襦下裙：当时秦漢到魏晋时期的初期襦裙，虽衣裳已分开，但外观上还保留了一些深衣的特征，比如袖仍是祛袂，下裳宽大，裙腰高长等特征。
                      </P>
                      <P>
                        冠帽巾：帽巾类得到空前的发展，基本后世有的在那时都有了。因冠已成为等级的标志，民间男子便发明了很多种巾帽。比如出现了幅巾，纶巾等。当时的冠出现了漆纱笼冠，为很重要的礼臣所戴。
                      </P>
                      <P>
                        直领：当时在已有交领，圆领的基础上，通过交领发明了直领。
                      </P>

